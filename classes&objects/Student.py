# It is possible to create your own data type for anything I want!

# Creating class Student (creating student data type)
class Student:

    # initialize function => you can map out what attributes a student should have
    # all of this attributes are associated to the class student
    def __init__(self, name, major, gpa, is_on_probation ):
        # assigning values
        self.name= name
        self.major= major
        self.gpa= gpa
        self.is_on_probation= is_on_probation